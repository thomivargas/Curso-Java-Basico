package com.company.Clases;

public class SmartDevice {
    String procedasador;
    String tipoPantalla;
    String color;
    String bateria;

    public SmartDevice(){}

    public SmartDevice(String procedasador, String tipoPantalla, String color, String bateria) {
        this.procedasador = procedasador;
        this.tipoPantalla = tipoPantalla;
        this.color = color;
        this.bateria = bateria;
    }


}
